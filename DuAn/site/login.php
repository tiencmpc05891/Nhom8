<?php
session_start();

require('../dao/khachhang.php'); 
require('../dao/PDOConnection.php'); 

$pdoConnection = new PDOConnection(); 
$loginDAO = new LoginDAO($pdoConnection->getConnection()); 
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $username = $_POST['username'];
    $password = $_POST['password'];

    
    if (empty($username) || empty($password)) {
        echo "<script>alert('Vui lòng điền đầy đủ thông tin tài khoản và mật khẩu.');</script>";
    } else {
        $customerResult = $loginDAO->loginCustomer($username, $password);
        if ($customerResult) {
           
            $_SESSION['user'] = $customerResult;
            header("Location: ../site/profile.php");
            exit();
        } else {
            $adminResult = $loginDAO->loginAdmin($username, $password);
            if ($adminResult) {
              
                $_SESSION['admin'] = $adminResult;
                header("Location: ../admin/index.php");
                exit();
            } else {
                
                echo "<script>alert('Tài khoản hoặc mật khẩu không đúng!');</script>";
            }
        }
    }
}

